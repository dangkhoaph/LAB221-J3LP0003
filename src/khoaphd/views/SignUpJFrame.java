    public boolean validateInput() {
        String email = txtEmail.getText().trim().toLowerCase();
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email is required as ID");
            txtEmail.requestFocus();
            return false;
        } else if (!email.matches("^[a-z][a-z0-9_\\-]*@[a-z]+(\\.[a-z]{2,3}){1,2}$")) {
            JOptionPane.showMessageDialog(this, "Invalid email");
            txtEmail.requestFocus();
            return false;
        }
        
        String password = new String(txtPassword.getPassword());
        String confirm = new String(txtConfirmPassword.getPassword());
        if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Password is required");
            txtPassword.requestFocus();
            return false;
        } else if (!confirm.equals(password)) {
            JOptionPane.showMessageDialog(this, "Confirm must match password");
            txtConfirmPassword.requestFocus();
            return false;
        }
        
        String fullName = txtFullName.getText().trim();
        if (fullName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Full name is required");
            txtFullName.requestFocus();
            return false;
        }
        
        String phone = txtPhone.getText().trim();
        if (phone.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Phone is required");
            txtPhone.requestFocus();
            return false;
        } else if (!phone.matches("^\\d{10}$")) {
            JOptionPane.showMessageDialog(this, "Phone must contain 10 digits");
            txtPhone.requestFocus();
            return false;
        }
        
        String address = txtAddress.getText().trim();
        if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Address is required");
            txtAddress.requestFocus();
            return false;
        }
        
        return true;
    }

