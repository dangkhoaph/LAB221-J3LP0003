    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        if (!validateInput()) {
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "Confirm signing up?");
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        
        String userID = txtEmail.getText().trim().toLowerCase();
        String password = new String(txtPassword.getPassword());
        String userName = txtFullName.getText().trim();
        String phone = txtPhone.getText().trim();
        String address = txtAddress.getText().trim();
        String role = "User";
        UserDTO dto = new UserDTO(userID, password, userName, phone, address, role);
        UserDAO dao = new UserDAO();
        try {
            boolean result = dao.insertNewUser(dto);
            if (result) {
                JOptionPane.showMessageDialog(this, "Sign up succeed");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Sign up failed");
            }
        } catch (ClassNotFoundException | SQLException e) {
            if (e.getMessage().contains("duplicate")) {
                JOptionPane.showMessageDialog(this, "ID " + userID + " already existed");
                txtEmail.requestFocus();
            } else {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSignUpActionPerformed

